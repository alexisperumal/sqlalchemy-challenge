@app.route("/api/v1.0/precipitation")
def precipitation():
    """Return Hawaii precipitation data as json"""
    prcp_data = {}
    for date, prcp in session.query(Measurement.date, Measurement.prcp).all():
        prcp_data[date] = prcp
    return jsonify(prcp_data)



